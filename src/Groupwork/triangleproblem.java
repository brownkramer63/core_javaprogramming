package Groupwork;

public class triangleproblem {
    public static void main(String[] args) {

        for (int i = 0; i <= 5; i++) {

            for (int j = 1; j <= i; j++) {
                System.out.print(i+" ");// make sure to use print not println

            }
            System.out.println();
        }
        for (int i = 0; i <= 5; i++) {
            for (int j = 1; j <= i; j++) {
                System.out.print(i+" ");
            }
            System.out.println();
        }
    }
}
