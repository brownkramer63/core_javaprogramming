package day04_concatenation;

public class ArithmeticOperators {
    public static void main(String[] args) {
        System.out.println(100+200);

        System.out.println(40-10);

        System.out.println(20/6); //since it was an integer will not give decimals

        System.out.println(20.0/6.0); // since it was a double answer was double

        System.out.println(20.0/6); //need a double in equation and it will give a decimal
        System.out.println(3+2+" "+4+5);





    }
}
