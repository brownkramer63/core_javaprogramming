package InterviewPrepQuestions;

public class Daysof4Weeks {
    public static void main(String[] args) {
        String weekprefix = "Week: ";
        String dayprefix = "Day: ";
        for (int i = 1; i <5 ; i++) {
            System.out.println(weekprefix+i);
            for (int j = 1; j <8 ; j++) {
                System.out.println(dayprefix+j);
            }



        }
    }
}
