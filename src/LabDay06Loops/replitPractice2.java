package LabDay06Loops;

public class replitPractice2 {
    public static void main(String[] args) {


        for(char i = 'A'; i<='Z'; i++){
            System.out.print(i+"");
        }
        System.out.print("\n");

        for (char j = 'z'; j >='a' ; j--) {
            System.out.print(j+"");
        }



    }
}
